a       = [[(0,0),(0,1),(0,2)], # Three of the moves
           [(0,0),(1,0),(2,0)], 
           [(0,0),(1,1),(2,2)],
           [(0,2),(1,2),(2,2)],
           [(0,2),(1,1),(2,0)],
           [(0,1),(1,1),(2,1)],
           [(1,0),(1,1),(1,2)],
           [(2,0),(2,1),(2,2)]]

b = []

start = 1
stop  = 2
step  = 2
print(a[0])
b = a[start:stop]  # items start through stop-1
print('1:2',b)
b = a[start:]      # items start through the rest of the array
print('1:',b)
b = a[:stop]       # items from the beginning through stop-1
print(':2',b)
b = a[:]           # a copy of the whole array
print(':',b)
print(id(b),id(a))

b = a[-1]    # last item in the array
print("-1",b)
b = a[-2:]   # last two items in the array
print('-2',b)
b = a[:-2]   # everything except the last two items
print(':-2',b) 



b = a[::-1]    # all items in the array, reversed
print('::-1',b)
b = a[1::-1]   # the first two items, reversed
print('1::-1',b)
b = a[:-3:-1]  # the last two items, reversed
print(':-3:-1',b)
b = a[-3::-1]  # everything except the last two items, reversed
print('-3::-1',b)

b = a[:4:2]    # the first four items, taken in an even way. ie (0,2)
print(':4:2',b)
b = a[:4:1] 
print(':4:1',b)# the first four items, taken 1 by 1

